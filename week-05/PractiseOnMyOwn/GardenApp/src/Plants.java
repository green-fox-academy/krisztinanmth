public class Plants {
  private String color;
  private String type;
  private int waterLevel;
  private double waterAbsorption;

  public Plants(String color, String type, int waterLevel, double waterAbsorption) {
    this.color = color;
    this.type = type;
    this.waterLevel = waterLevel;
    this.waterAbsorption = waterAbsorption;
  }
}
